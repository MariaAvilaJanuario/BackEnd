// Faça um algoritmo que leia os valores de A, B, C 
// Em seguida imprima na tela a soma entre A e B é mostre se a soma é menor que C.

let A = 16;
let B = 8;
let C = 20;
let soma = (A + B);
console.log("A soma entre A e B é: " + soma);

if (soma == C) {
    console.log ("A soma é igual a C")
}
else if (soma > C) {
    console.log ("A soma é maior do que C")
} else {
    console.log ("A soma é menor do que C")
}